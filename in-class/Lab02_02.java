public class Lab02_02 {
  public static void main (String [] args) {
    // 02. Increment and Decrement
    int x = 3;
    x++;
    System.out.println(x);
    x = 3;
    ++x;
    System.out.println(x);
    
    int n = 8;
    int result = n++;
    System.out.println("n = " + n + ", result = " + result);
    
    int n = 8;
    int result = ++n;
    System.out.println("n = " );
  }
}